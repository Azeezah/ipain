def scale_img_map(html, in_x, in_y, out_x, out_y):
    out_html = []
    xscale = out_x / in_x
    for line in html.split('\n'):
        if not set("0123456789").intersection(line): continue
        line_parts = line.split("\"")
        coords = line_parts[-4].split(',')
        xs = map(int, coords[::2])
        ys = map(int, coords[1::2])
        xs = [x * out_x / in_x for x in xs]
        ys = [y * out_y / in_y for y in ys]
        line_parts[-4] = ','.join(str(round(x))+','+str(round(y)) for x, y in zip(xs, ys))
        line = '\"'.join(line_parts)
        out_html.append(line)
    return '\n'.join(out_html)

